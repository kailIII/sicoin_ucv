# coding: utf8
def devolver_mes(mes):
    if mes == 1:
        mes= "enero"
    elif mes == 2:
        mes= "febrero"
    elif mes == 3:
        mes= "marzo"
    elif mes == 4:
        mes= "abril"
    elif mes == 5:
        mes= "mayo"
    elif mes == 6:
        mes= "junio"
    elif mes == 7:
        mes= "julio"
    elif mes == 8:
        mes= "agosto"
    elif mes == 9:
        mes= "septiembre"    
    elif mes == 10:
        mes= "octubre"
    elif mes == 11:
        mes= "noviembre"
    else:
        mes = "diciembre"
    return mes
